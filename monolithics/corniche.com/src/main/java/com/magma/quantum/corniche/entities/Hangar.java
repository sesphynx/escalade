package com.magma.quantum.corniche.entities;

public class Hangar extends Logis {
	
	public int pieces ;
	public boolean videoCablage ;
	public int   nombrePriseEcran ;
	public boolean sonoCablage ;
	public boolean amenagement ; 
	public String capacite ;
	public int placesAssises ;
	public int nbreLoges ;
	
	public int getPieces() {
		return pieces;
	}
	public void setPieces(int pieces) {
		this.pieces = pieces;
	}
	public boolean isVideoCablage() {
		return videoCablage;
	}
	public void setVideoCablage(boolean videoCablage) {
		this.videoCablage = videoCablage;
	}
	public int getNombrePriseEcran() {
		return nombrePriseEcran;
	}
	public void setNombrePriseEcran(int nombrePriseEcran) {
		this.nombrePriseEcran = nombrePriseEcran;
	}
	public boolean isSonoCablage() {
		return sonoCablage;
	}
	public void setSonoCablage(boolean sonoCablage) {
		this.sonoCablage = sonoCablage;
	}
	public boolean isAmenagement() {
		return amenagement;
	}
	public void setAmenagement(boolean amenagement) {
		this.amenagement = amenagement;
	}
	public String getCapacite() {
		return capacite;
	}
	public void setCapacite(String capacite) {
		this.capacite = capacite;
	}
	public int getPlacesAssises() {
		return placesAssises;
	}
	public void setPlacesAssises(int placesAssises) {
		this.placesAssises = placesAssises;
	}
	public int getNbreLoges() {
		return nbreLoges;
	}
	public void setNbreLoges(int nbreLoges) {
		this.nbreLoges = nbreLoges;
	}
	
	
	

}
